const month = 3
switch (month) {      
    case 1: 
        console.log("Month is January");
        break;
    case 2: 
        console.log("Month is February");
        break;
    case 3: 
        console.log("Month is march");
        break;
    case 4: 
        console.log("Month is April");
        break;
    default:
        console.log("Default Case");
        break;
}




